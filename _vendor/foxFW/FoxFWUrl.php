<?php
//++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
class FoxFWUrl
{
	private $racine = '/';
	private $path   = '';
	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function __construct( $racine, $path = '' ) 
    {
    	$this->racine = $racine;
    	$this->path   = $path;
    }

   	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function addDir( $url )
	{
		if( $url != '.' )
		if( $url != '..')
			$this->path .= $url.'/';
	}

	   //--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function addFile( $url )
	{
		$this->path .= $url;
	}

	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function retrograde()
	{
		$tab = explode('/', $this->path );
		array_pop( $tab );
		array_pop( $tab );
		$url = implode('/', $tab );

		if( $url != '' )
			$url .= '/';
		$this->path = $url;
	}

	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function get()
	{
		return $this->racine . $this->path;
	}

	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function getPath()
	{
		return $this->path;
	}

	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function getRacine()
	{
		return $this->racine;
	}

	//--------------------------------------------------------------------------------
	//--------------------------------------------------------------------------------
	//
	//
	//
	//--------------------------------------------------------------------------------
	public function getNoUrl()
	{
		return _RACINE.$this->get();
	}
};